// 3. Uso do if-else
//Descrição: Crie uma variável int chamada nota e atribua a ela um valor entre 
//0 e 100. Use uma estrutura if-else para imprimir "Aprovado" se a nota for 60 ou
//mais, e "Reprovado" caso contrário.

main() {
  // Declaração de Variaveis
  int nota = 25;

  // Estrutur de Decisão (Verificação de aprovação)
  if (nota >= 60) {
    print('Aprovado');
  } else {
    print('Reprovado');
  }
}
