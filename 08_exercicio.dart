//Descrição: Crie uma função chamada soma que recebe dois números inteiros e
//retorna a soma deles. Imprima o resultado.

main() {
  // Declaração de Variaveis
  int num1 = 2;
  int num2 = 5;

  // Chamada da Função
   print('A soma entre $num1 e $num2 é igual a ${funcao_somar(num1, num2)}');
}

// Criação da Função COM Parametro e COM Retorno
int funcao_somar(int num1, int num2) {
  return num1 + num2;
}
